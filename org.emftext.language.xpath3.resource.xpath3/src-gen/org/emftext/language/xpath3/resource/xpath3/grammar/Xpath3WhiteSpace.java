package org.emftext.language.xpath3.resource.xpath3.grammar;


public class Xpath3WhiteSpace extends org.emftext.language.xpath3.resource.xpath3.grammar.Xpath3FormattingElement {
	
	private final int amount;
	
	public Xpath3WhiteSpace(int amount, org.emftext.language.xpath3.resource.xpath3.grammar.Xpath3Cardinality cardinality) {
		super(cardinality);
		this.amount = amount;
	}
	
	public int getAmount() {
		return amount;
	}
	
	public String toString() {
		return "#" + getAmount();
	}
	
}
