package org.emftext.language.xpath3.resource.xpath3.mopp;

import java.util.Collections;
import java.util.Set;
import org.eclipse.emf.ecore.EStructuralFeature;

/**
 * A representation for a range in a document where a structural feature (e.g., a
 * reference) is expected.
 */
public class Xpath3ExpectedStructuralFeature extends org.emftext.language.xpath3.resource.xpath3.mopp.Xpath3AbstractExpectedElement {
	
	private org.emftext.language.xpath3.resource.xpath3.grammar.Xpath3Placeholder placeholder;
	
	public Xpath3ExpectedStructuralFeature(org.emftext.language.xpath3.resource.xpath3.grammar.Xpath3Placeholder placeholder) {
		super(placeholder.getMetaclass());
		this.placeholder = placeholder;
	}
	
	public EStructuralFeature getFeature() {
		return placeholder.getFeature();
	}
	
	/**
	 * Returns the expected placeholder.
	 */
	public org.emftext.language.xpath3.resource.xpath3.grammar.Xpath3SyntaxElement getSyntaxElement() {
		return placeholder;
	}
	
	public String getTokenName() {
		return placeholder.getTokenName();
	}
	
	public Set<String> getTokenNames() {
		return Collections.singleton(getTokenName());
	}
	
	public String toString() {
		return "EFeature " + getFeature().getEContainingClass().getName() + "." + getFeature().getName();
	}
	
	public boolean equals(Object o) {
		if (o instanceof Xpath3ExpectedStructuralFeature) {
			return getFeature().equals(((Xpath3ExpectedStructuralFeature) o).getFeature());
		}
		return false;
	}
	@Override
	public int hashCode() {
		return getFeature().hashCode();
	}
	
}
