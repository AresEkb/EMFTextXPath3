package org.emftext.language.xpath3.resource.xpath3.ui;

import org.eclipse.emf.ecore.EObject;

public class Xpath3HoverTextProvider implements org.emftext.language.xpath3.resource.xpath3.IXpath3HoverTextProvider {
	
	private org.emftext.language.xpath3.resource.xpath3.ui.Xpath3DefaultHoverTextProvider defaultProvider = new org.emftext.language.xpath3.resource.xpath3.ui.Xpath3DefaultHoverTextProvider();
	
	public String getHoverText(EObject container, EObject referencedObject) {
		// Set option overrideHoverTextProvider to false and customize this method to
		// obtain custom hover texts.
		return defaultProvider.getHoverText(referencedObject);
	}
	
	public String getHoverText(EObject object) {
		// Set option overrideHoverTextProvider to false and customize this method to
		// obtain custom hover texts.
		return defaultProvider.getHoverText(object);
	}
	
}
